"""
SignBridge ML — Dataset Generator
==================================
Builds a synthetic training dataset from the 210 ASL sign definitions
in signs_data.json (exported from sign-mapper.js).

Each sign is a point in 12-dimensional feature space:
  [r_elbow_x, r_elbow_y, r_wrist_x, r_wrist_y,
   l_elbow_x, l_elbow_y, l_wrist_x, l_wrist_y,
   rHand_enc, lHand_enc, expression_enc, rMotion_enc]

Coordinates are normalised to [0, 1] using the SVG ViewBox (360×240).
Categorical features are label-encoded.

Synthetic samples are generated by adding Gaussian noise around each
sign's prototype — simulating natural variation in human signing.
"""

import json
import os
import numpy as np
import pandas as pd
from pathlib import Path

# ── Constants ─────────────────────────────────────────────────────────────────

VIEWBOX_W = 360.0
VIEWBOX_H = 240.0

HAND_SHAPES = ['NONE','FLAT','FIST','POINT','V','C','OK',
               'THUMB_UP','ILY','CLAW','HORNS','L','A','O']

EXPRESSIONS = ['NEUTRAL','HAPPY','QUESTION','WH_QUESTION','NEGATIVE','EMPHATIC']

MOTIONS = ['NONE','motion-wave','motion-circle','motion-push','motion-pull',
           'motion-nod','motion-shake','motion-lift','motion-flick',
           'motion-tap','motion-wiggle','motion-roll','motion-flip']

HAND_ENC  = {h: i for i, h in enumerate(HAND_SHAPES)}
EXPR_ENC  = {e: i for i, e in enumerate(EXPRESSIONS)}
MOT_ENC   = {m: i for i, m in enumerate(MOTIONS)}

FEATURE_NAMES = [
    'r_elbow_x', 'r_elbow_y', 'r_wrist_x', 'r_wrist_y',
    'l_elbow_x', 'l_elbow_y', 'l_wrist_x', 'l_wrist_y',
    'rHand', 'lHand', 'expression', 'rMotion',
]

_DATA_PATH = Path(__file__).parent / 'signs_data.json'


# ── Encoding ──────────────────────────────────────────────────────────────────

def _encode_sign(sign: dict) -> np.ndarray:
    """Convert one sign dict to a normalised feature vector."""
    return np.array([
        sign['right_elbow_x'] / VIEWBOX_W,
        sign['right_elbow_y'] / VIEWBOX_H,
        sign['right_wrist_x'] / VIEWBOX_W,
        sign['right_wrist_y'] / VIEWBOX_H,
        sign['left_elbow_x']  / VIEWBOX_W,
        sign['left_elbow_y']  / VIEWBOX_H,
        sign['left_wrist_x']  / VIEWBOX_W,
        sign['left_wrist_y']  / VIEWBOX_H,
        HAND_ENC.get(sign['rHand'], 0) / len(HAND_SHAPES),
        HAND_ENC.get(sign['lHand'], 0) / len(HAND_SHAPES),
        EXPR_ENC.get(sign['expression'], 0) / len(EXPRESSIONS),
        MOT_ENC.get(sign['rMotion'], 0)  / len(MOTIONS),
    ], dtype=np.float32)


# ── Dataset generation ────────────────────────────────────────────────────────

def load_signs() -> dict:
    with open(_DATA_PATH, 'r', encoding='utf-8') as f:
        return json.load(f)


def generate_dataset(
    samples_per_sign: int = 150,
    noise_coord: float = 0.015,   # Gaussian σ on normalised coords (~5px)
    noise_cat: float   = 0.005,   # tiny noise on categorical encodings
    random_state: int  = 42,
) -> tuple[np.ndarray, np.ndarray, list[str]]:
    """
    Generate a synthetic training dataset.

    Returns
    -------
    X : np.ndarray, shape (n_samples, 12)
    y : np.ndarray, shape (n_samples,) — integer class labels
    classes : list[str]               — class name for each integer label
    """
    rng    = np.random.default_rng(random_state)
    signs  = load_signs()
    labels = sorted(signs.keys())               # deterministic class order
    label_map = {name: i for i, name in enumerate(labels)}

    X_rows, y_rows = [], []

    for sign_name, sign_data in signs.items():
        proto  = _encode_sign(sign_data)        # prototype feature vector
        noise  = rng.normal(0, noise_coord, size=(samples_per_sign, 12)).astype(np.float32)
        noise[:, 8:] *= noise_cat / noise_coord  # less noise on categoricals

        samples = np.clip(proto + noise, 0.0, 1.0)
        X_rows.append(samples)
        y_rows.extend([label_map[sign_name]] * samples_per_sign)

    X = np.vstack(X_rows)
    y = np.array(y_rows, dtype=np.int32)
    return X, y, labels


def to_dataframe(X: np.ndarray, y: np.ndarray, classes: list[str]) -> pd.DataFrame:
    """Wrap dataset in a labelled DataFrame for EDA."""
    df = pd.DataFrame(X, columns=FEATURE_NAMES)
    df['label'] = y
    df['sign']  = [classes[i] for i in y]
    return df


# ── WLASL real-data loader ─────────────────────────────────────────────────────

_WLASL_FEATURES_DIR = Path(__file__).parent / 'wlasl_features'


def load_wlasl_dataset(
    features_dir: Path | None = None,
    use_sequences: bool = False,
) -> tuple[np.ndarray, np.ndarray, list[str]]:
    """
    Load pre-extracted WLASL features.

    Parameters
    ----------
    features_dir  : path to directory containing X.npy / y.npy / classes.json
    use_sequences : if True, loads X_seq.npy (N, seq, 186)

    Returns
    -------
    X       : np.ndarray  (N, D) or (N, S, D)
    y       : np.ndarray  (N,)
    classes : list[str]
    """
    d = Path(features_dir) if features_dir else _WLASL_FEATURES_DIR

    prefix = 'X_seq' if use_sequences else 'X'
    x_path = d / f'{prefix}.npy'
    y_path = d / 'y.npy'
    c_path = d / 'classes.json'

    if not x_path.exists():
        msg = f"Feature matrix not found: {x_path}"
        if use_sequences:
            msg += "\nEnsure you ran extraction with save_sequences=True."
        raise FileNotFoundError(msg)

    X = np.load(str(x_path))
    y = np.load(str(y_path))
    with open(c_path, 'r') as f:
        classes = json.load(f)

    return X.astype(np.float32), y.astype(np.int32), classes


def wlasl_features_available(features_dir: Path | None = None) -> bool:
    """Return True if pre-extracted WLASL features exist on disk."""
    d = Path(features_dir) if features_dir else _WLASL_FEATURES_DIR
    return (d / 'X.npy').exists()


def encode_landmarks(landmarks: list[dict]) -> np.ndarray:
    """
    Convert a list of 21 MediaPipe landmarks (real inference) to our
    12-feature vector so the trained model can classify live hand poses.

    landmarks: [{'x':0.5,'y':0.5,'z':0.0,'visibility':1.0}, ...]  (21 items)
    """
    if len(landmarks) < 9:
        return np.zeros(12, dtype=np.float32)

    # MediaPipe hand landmark indices:
    # 0=wrist, 5=index_base (≈ our elbow proxy), 8=index_tip (≈ our wrist)
    wrist = landmarks[0]
    elbow = landmarks[5]
    tip   = landmarks[8]

    # Map to our normalised coordinate space
    feat = np.array([
        elbow['x'], elbow['y'],   # r_elbow (proxy)
        tip['x'],   tip['y'],     # r_wrist (proxy)
        wrist['x'], wrist['y'],   # l_elbow (proxy — single hand: mirror)
        wrist['x'], wrist['y'],   # l_wrist
        0.0, 0.0, 0.0, 0.0,      # categorical features (unknown at inference)
    ], dtype=np.float32)

    return np.clip(feat, 0.0, 1.0)
